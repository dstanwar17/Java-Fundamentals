public class practiceProblem {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println(i + ",");
        }

        int a = 1;
        while (a < 4) {
            System.out.print(a + " ");
            a++;
        }

    }
}
