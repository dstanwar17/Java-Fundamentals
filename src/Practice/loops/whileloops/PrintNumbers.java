/**
 * @author dstanwar17
 */
public class PrintNumbers {
    public static void main(String[] args) {
        // Print Numbers from 1 to 10 using While Loop

        int i = 1;

        while (i < 11) {
            System.out.println(i);
            i++;
        }

    }
}
